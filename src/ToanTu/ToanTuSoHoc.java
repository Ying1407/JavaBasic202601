package ToanTu;

public class ToanTuSoHoc {
    public static void main(String[] args) {
        int a = 25;
        int b = 20;

        System.out.println(a % b);

        a++; //Tăng lên 1 đơn vị,tương ứng a = a+1 - bước toán tử
        System.out.println(a); //hoặc println (a = a+1)

        b*=5; // nhân và gán giá trị b = b*5
        System.out.println(b);

        System.out.println(a = 5);
        System.out.println( a > 5);


    }


}
